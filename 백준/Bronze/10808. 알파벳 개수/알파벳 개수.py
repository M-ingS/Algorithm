s = input()
cnt = [0] * 26 # 각 알파벳의 개수를 세어줄 cnt변수 26개 생성

for i in s:
    cnt[ord(i) - 97] = cnt[ord(i) - 97] + 1
#print(*cnt) #각 리스트의 요소를 분리해서 공백을 사용하여 출력해줌
for i in cnt:
    print(i, end=' ')
# 'a'를 처리할 때: ord('a') - 97은 'a'의 아스키 코드(97)에서
# 'a'의 아스키 코드(97)를 빼서 0이 됩니다.  --> cnt[0]의 값이 1 증가
# 'k'를 처리할 때: ord('k') - 97은 'k'의 아스키 코드(107)에서
# 'a'의 아스키 코드(97)를 빼서 10이 됩니다. --> cnt[10]의 값이 1 증가